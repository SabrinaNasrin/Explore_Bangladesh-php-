<h3 class=".bangla_font"><span style="color: #000000;"><strong>আমার সোনার বাংলা <i>(</i><i>Bangla</i><i>)</i></strong></span></h3>
<p><span style="color: #000000;">আমার সোনার বাংলা,</span><br>
<span style="color: #000000;"> আমি তোমায় ভালবাসি।</span><br>
<span style="color: #000000;"> চিরদিন তোমার আকাশ,</span><br>
<span style="color: #000000;"> তোমার বাতাস</span><br>
<span style="color: #000000;"> আমার প্রাণে বাজায় বাঁশি।</span></p>
<p><span style="color: #000000;">ও মা,</span><br>
<span style="color: #000000;"> ফাগুনে তোর আমের বনে</span><br>
<span style="color: #000000;"> ঘ্রাণে পাগল করে</span><br>
<span style="color: #000000;"> মরি হায়, হায় রে</span><br>
<span style="color: #000000;"> ও মা,</span><br>
<span style="color: #000000;"> অঘ্রানে তোর ভরা ক্ষেতে,</span><br>
<span style="color: #000000;"> আমি কী দেখেছি মধুর হাসি।।</span></p>
<p><span style="color: #000000;">কী শোভা, কী ছায়া গো,</span><br>
<span style="color: #000000;"> কী স্নেহ, কী মায়া গো,</span><br>
<span style="color: #000000;"> কী আঁচল বিছায়েছ</span><br>
<span style="color: #000000;"> বটের মূলে,</span><br>
<span style="color: #000000;"> নদীর কূলে কূলে।</span></p>
<p><span style="color: #000000;">মা, তোর মুখের বাণী</span><br>
<span style="color: #000000;"> আমার কানে লাগে</span><br>
<span style="color: #000000;"> সুধার মতো-</span></p>
<p><span style="color: #000000;">মা তোর বদন খানি মলিন হলে</span><br>
