<h3><span style="color: #000000;"><b>My beloved Bengal (Literal English Translation)</b></span></h3>
<p><span style="color: #000000;">My Bengal of Gold,</span><br>
<span style="color: #000000;"> I love you.</span></p>
<p><span style="color: #000000;">Forever your skies,</span><br>
<span style="color: #000000;"> Your air set my heart in tune</span><br>
<span style="color: #000000;"> As if it were a flute.</span></p>
<p><span style="color: #000000;">In spring, O mother mine,</span><br>
<span style="color: #000000;"> The fragrance from your mango groves</span><br>
<span style="color: #000000;"> Makes me wild with joy,</span><br>
<span style="color: #000000;"> Ah, what a thrill!</span><br>
<span style="color: #000000;"> In autumn, O mother mine,</span><br>
<span style="color: #000000;"> In the full blossomed paddy fields</span><br>
<span style="color: #000000;"> I have seen spread all over sweet smiles.</span></p>
<p><span style="color: #000000;">Ah, what beauty, what shades,</span><br>
<span style="color: #000000;"> What an affection, and what tenderness!</span><br>
<span style="color: #000000;"> What a quilt have you spread</span><br>
<span style="color: #000000;"> At the feet of banyan trees</span><br>
<span style="color: #000000;"> And along the banks of rivers!</span></p>
<p><span style="color: #000000;">Oh mother mine, words from your lips</span><br>
<span style="color: #000000;"> Are like nectar to my ears.</span><br>
<span style="color: #000000;"> Ah, what a thrill!</span></p>
<p><span style="color: #000000;">If sadness, O mother mine,</span><br>
<span style="color: #000000;"> Casts a gloom on your face,</span><br>
<span style="color: #000000;"> My eyes are filled with tears!</span><br>
<span style="color: #000000;"> Golden Bengal,</span><br>
<span style="color: #000000;"> I love you.</span></p>
