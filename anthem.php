<h3><span style="color: #000000;"><b>Amar sonar Bangla <i>(Transliteration)</i></b></span></h3>
<p><span style="color: #000000;">Amar sonar Bangla</span><br>
<span style="color: #000000;"> Ami tomay bhalobashi</span></p>
<p><span style="color: #000000;">Chirodin tomar akash,</span><br>
<span style="color: #000000;"> Tomar batas,</span><br>
<span style="color: #000000;"> Amar prane bajay bashi.</span></p>
<p><span style="color: #000000;">O ma</span><br>
<span style="color: #000000;"> Fagune tor amer bone</span><br>
<span style="color: #000000;"> Ghrane pagol kore,</span><br>
<span style="color: #000000;"> Mori hay, hay re,</span><br>
<span style="color: #000000;"> O ma,</span><br>
<span style="color: #000000;"> Oghrane tor bhora khete</span><br>
<span style="color: #000000;"> Ami ki dekhechhi modhur hasi.</span></p>
<p><span style="color: #000000;">Ki shobha, ki chhaya go,</span><br>
<span style="color: #000000;"> Ki sneho, ki maya go,</span><br>
<span style="color: #000000;"> Ki achol bichhayechho</span><br>
<span style="color: #000000;"> Boter mule,</span><br>
<span style="color: #000000;"> Nodir kule kule.</span></p>
<p><span style="color: #000000;">Ma, tor mukher bani</span><br>
<span style="color: #000000;"> Amar kane lage</span><br>
<span style="color: #000000;"> Sudhar moto-</span></p>
<p><span style="color: #000000;">Ma tor bodon khani molin hole</span><br>
<span style="color: #000000;"> ami noyon</span><br>
<span style="color: #000000;"> o ma ami noyon jole bhasi</span><br>
<span style="color: #000000;"> sonar Bangla,</span><br>
<span style="color: #000000;"> ami tomay bhalobashi!</span></p>
